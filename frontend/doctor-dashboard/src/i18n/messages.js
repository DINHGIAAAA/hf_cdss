function resolvePath(tree, key) {
  return key.split(".").reduce((value, part) => value?.[part], tree);
}

export function translate(language, key, vars = {}) {
  const locales = [language, "en"];
  let value;
  for (const locale of locales) {
    value = resolvePath(MESSAGES[locale], key);
    if (value != null) break;
  }
  if (Array.isArray(value)) return value;
  if (typeof value !== "string") return key;
  return value.replace(/\{(\w+)\}/g, (_, name) => vars[name] ?? `{${name}}`);
}

export function streamStatusLabel(language, step) {
  const key = `chat.stream.${step}`;
  const label = translate(language, key);
  return label === key ? translate(language, "chat.stream.processing") : label;
}
